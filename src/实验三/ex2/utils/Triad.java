public class Triad {

    private int upperNums;
    private int lowerNums;
    private int nonEnglishNums;

    public int getUpperNums() {
        return upperNums;
    }

    public void setUpperNums(int upperNums) {
        this.upperNums = upperNums;
    }

    public int getLowerNums() {
        return lowerNums;
    }

    public void setLowerNums(int lowerNums) {
        this.lowerNums = lowerNums;
    }

    public int getNonEnglishNums() {
        return nonEnglishNums;
    }

    public void setNonEnglishNums(int nonEnglishNums) {
        this.nonEnglishNums = nonEnglishNums;
    }
}
